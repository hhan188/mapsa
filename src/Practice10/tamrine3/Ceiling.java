package Practice10.tamrine3;

public class Ceiling {
    private int height;
    private int pointedColor;

    public Ceiling(int height, int pointedColor) {
        this.height = height;
        this.pointedColor = pointedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPointedColor() {
        return pointedColor;
    }
}
